
/*

	문제 05-1 [원형 연결 리스트의 활용]

	앞서 구현한 원형 연결 리스트를 기반으로 다음 내옹을 모드 담고있는 프로그램을 작성해 보자.

		- 직원정보를 등록할 수 있다. 직원 정보는 사번과 이름으로 구성된다.
				직원정보를 담을 수 있는 구조체를 정의하고, 이를 기반으로 대략 4명의 직원정보를 원형 연결 리스트에 저장한다.
				4명의 직원정보는 임의로 결정하기로 하고(프로그램 사용자로부터 입력받지 않아도 된다), 
				직원의 사번은 int형 변수에 담을 수 있다고 가정하자.
				그리고 원형 연결 리스트에는 구조체 변수의 주소 값을 저장하는 것을 원칙으로 하자.

		- 직원은 순서대로 돌아가면서 당직을 선다.
				당직의 순서는 프로그램에 등록되는 등록 순서를 기준으로 결정된다.

		- 직원의 이름과 하나의 숫자를 이용해서 당직자를 확인한다.
				함수를 하나 정의하자. 이 함수는 직원의 이름과 숫자를 인자로 전달받는다.
				그러면 전달된 이름의 직원이 당직을 선 후로, 전달된 숫자에 해당하는 만큼의 날이 지나서 당직을 서게 되는 직원의 정보를 반환한다.
				예를 들어서 '이수정'과 숫자 7이 전달되면 이수정이 당직을 선 후로 7일 뒤에 누가 당직을 서는지에 대한 정보가 반환되어야 한다.

*/


